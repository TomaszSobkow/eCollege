package topic3;


public class Assignemt3_5B {
    public static void main(String[] args) {

        int x = 0, y = 10;
        while (x <10){
            System.out.println("Going Up: "+ x +" - Going Down: "+ y);

            if(x == y)
                break;

            x++;
            y--;

        }
    }
}
